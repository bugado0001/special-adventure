export const Soma = (n1, n2) =>{ 
    return n1 + n2
}

export const subtração = (n1, n2) =>{ 
    return n1 - n2
}

export const Divisao = (n1, n2) =>{ 
    return n1 / n2
}
export const multiplicacao = (n1, n2) =>{ 
    return n1 * n2
}


